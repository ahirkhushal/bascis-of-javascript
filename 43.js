//function expression

//when we give a variable to function then its called function expression.

//for example

//this is function declaration (normal function)

function mynameiskhushal() {
  console.log("khushal is 18 year old... ==> `function declaration`");
}

mynameiskhushal();

//this is function expression of the same example ( function expression)

const mynameisKhushal = function () {
  console.log("khushal is 18 year old... ==> `function expression`");
};

mynameisKhushal();
